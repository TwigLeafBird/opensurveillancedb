	<p class="m-0 opacity-80">
		opensurveillancedb is a community project cataloguing the various types of surveillance devices
		used in our everyday lives and infrastructure. Think of seeing a camera on a street light and
		going "I don't know what that is or what it can do" - the goal of this site is to make answering
		that question easier. By identifying key visual characteristics of the devices we encounter, the
		goal is to narrow down what a device may possibly be, and from that gain a general understanding
		of what it may be doing.
